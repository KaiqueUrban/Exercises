#Faça um algoritmo para somar dois números e multiplicar o resultado pelo primeiro.

#entradas
num1 = int(input("Insira o primeiro dígito "))
num2 = int(input("Insita o segundo dígito "))
#processamento
soma = num1 + num2
multiplicacao = soma * num1
#saída
print(f"O valor da operação é: {multiplicacao}")